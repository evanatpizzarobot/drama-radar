// Generate KV bulk put entries
function generateKvEntries() {
  const entries = [];

  // Load existing cast index and merge
  const existingIndexPath = path.join(__dirname, "..", "cast-kv-entries.json");
  let existingSlugs = [];
  if (fs.existsSync(existingIndexPath)) {
    const existingEntries = JSON.parse(fs.readFileSync(existingIndexPath, "utf-8"));
    const indexEntry = existingEntries.find((e) => e.key === "cast:index");
    if (indexEntry) {
      existingSlugs = JSON.parse(indexEntry.value);
    }
  }

  // Combine existing + new slugs
  const newSlugs = CAST_MEMBERS.map((m) => m.slug);
  const allSlugs = [...new Set([...existingSlugs, ...newSlugs])];

  // Add the updated index entry
  entries.push({
    key: "cast:index",
    value: JSON.stringify(allSlugs),
  });

  // Add each cast member
  for (const member of CAST_MEMBERS) {
    const fullMember = {
      ...member,
      createdAt: NOW,
      updatedAt: NOW,
    };

    entries.push({
      key: `cast:${member.slug}`,
      value: JSON.stringify(fullMember),
    });
  }

  return entries;
}

const entries = generateKvEntries();

