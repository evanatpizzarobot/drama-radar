// Generate KV bulk put entries
function generateKvEntries() {
  const entries = [];

  const batch4Path = path.join(__dirname, "..", "cast-kv-entries-batch4.json");
  let existingSlugs = [];
  if (fs.existsSync(batch4Path)) {
    const data = JSON.parse(fs.readFileSync(batch4Path, "utf-8"));
    const indexEntry = data.find((e) => e.key === "cast:index");
    if (indexEntry) {
      existingSlugs = JSON.parse(indexEntry.value);
    }
  }

  const newSlugs = CAST_MEMBERS.map((m) => m.slug);
  const allSlugs = [...new Set([...existingSlugs, ...newSlugs])];

  entries.push({
    key: "cast:index",
    value: JSON.stringify(allSlugs),
  });

  for (const member of CAST_MEMBERS) {
    entries.push({
      key: `cast:${member.slug}`,
      value: JSON.stringify({ ...member, createdAt: NOW, updatedAt: NOW }),
    });
  }

  return entries;
}

