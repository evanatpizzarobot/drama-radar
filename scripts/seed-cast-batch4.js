// Generate KV bulk put entries
function generateKvEntries() {
  const entries = [];

  // Load existing cast index from batch 3
  const batch3Path = path.join(__dirname, "..", "cast-kv-entries-batch3.json");
  let existingSlugs = [];
  if (fs.existsSync(batch3Path)) {
    const data = JSON.parse(fs.readFileSync(batch3Path, "utf-8"));
    const indexEntry = data.find((e) => e.key === "cast:index");
    if (indexEntry) {
      existingSlugs = JSON.parse(indexEntry.value);
    }
  }

  // Combine existing + new slugs
  const newSlugs = CAST_MEMBERS.map((m) => m.slug);
  const allSlugs = [...new Set([...existingSlugs, ...newSlugs])];

  entries.push({
    key: "cast:index",
    value: JSON.stringify(allSlugs),
  });

  for (const member of CAST_MEMBERS) {
    const fullMember = {
      ...member,
      createdAt: NOW,
      updatedAt: NOW,
    };
    entries.push({
      key: `cast:${member.slug}`,
      value: JSON.stringify(fullMember),
    });
  }

  return entries;
}

const entries = generateKvEntries();
const outputPath = path.join(__dirname, "..", "cast-kv-entries-batch4.json");
fs.writeFileSync(outputPath, JSON.stringify(entries, null, 2));

